    public void salvar() {

        String descricao = txtDescricao.getText().toUpperCase();
        String preco = txtPreco.getText();

        if (descricao.isEmpty() || preco.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Favor Preencher todos os Campos!", "ERRO", JOptionPane.WARNING_MESSAGE);
        }
        Produto produto = new Produto(descricao, Double.parseDouble(preco));
        try {
            new ProdutoDaoIMP(conn).insert(produto);

        } catch (Exception e) {
            throw e;
        }

        txtDescricao.setText("");
        txtPreco.setText("");
    }

